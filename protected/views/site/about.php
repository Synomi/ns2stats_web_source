<h1>NS2Stats</h1>
<p>NS2Stats offers wide variety of statistics for players of PC game Natural Selection 2. NS2Stats includes ELO ranking, build orders, and various other player, mod, map, server and round statistics.
<p>NS2Stats uses data sent by servers that have NS2Stats mod installed in them. You can install the mod by following <?php echo CHtml::link('these instructions', array('install')) ?>.</p>

